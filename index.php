    <h1 id="Frecuentes_title">Preguntas Frecuentes</h1>
    <details>
      <summary>¿Qué diferencia hay entre transtornos psicológicos y uno psiquiátrico?</summary>
      <p>La principal diferencia entre un trastorno psicológico y uno psiquiátrico es que en psicología se habla de trastornos mentales, mientras que en psiquiatría se habla de enfermedades mentales. Los problemas mentales se identifican en base a la experiencia y la observación de una variedad de síntomas que culminan en un diagnóstico psiquiátrico. Con problemas psicológicos, el médico formula una hipótesis basada en los síntomas además de los factores sociales y ambientales que afectan al paciente.</p>
    </details>
    <details>
      <summary>¿Qué es la depresión?</summary>
      <p>La depresión es un trastorno del estado de ánimo que se caracteriza por una tristeza persistente, una pérdida de interés por las actividades que solían ser placenteras y una baja autoestima. La depresión puede afectar a la salud física, mental y emocional de las personas, y puede interferir con su funcionamiento diario.</p>
    </details>
    <details>
      <summary>¿Qué es el estres y cómo se puede manejar?</summary>
      <p>El estrés es una reacción del cuerpo y la mente ante situaciones que nos exigen o amenazan. El estrés puede tener efectos negativos en nuestra salud física y mental, como ansiedad, irritabilidad, insomnio, dolores de cabeza, problemas digestivos, etc. Para manejar el estrés, se recomienda identificar y evitar las fuentes de estrés, practicar técnicas de relajación, hacer ejercicio, llevar una alimentación saludable, dormir bien, tener una actitud positiva y buscar apoyo social o profesional si es necesario.</p>
    </details>
    <details>
      <summary>¿Qué es la auto estima y cómo se ouede mejorar?</summary>
      <p>La autoestima es el valor que nos damos a nosotros mismos, basado en nuestra percepción de nuestras capacidades, cualidades, logros y defectos. La autoestima influye en nuestra confianza, seguridad, motivación y bienestar. Para mejorar la autoestima, se recomienda aceptarnos y querernos como somos, reconocer y potenciar nuestros talentos, establecer y cumplir metas realistas, ser auténticos y honestos, respetar nuestros derechos y límites, cuidar nuestra imagen y salud, y rodearnos de personas que nos aprecien y respeten.</p>
    </details>
    <details>
      <summary>¿Qué es la inteligencia emocional y cómo se puede desarrollar?</summary>
      <p>La inteligencia emocional es la capacidad de reconocer, comprender, expresar y regular nuestras propias emociones y las de los demás, así como de usarlas de forma adecuada para adaptarnos a las diferentes situaciones. La inteligencia emocional nos ayuda a mejorar nuestras relaciones, comunicación, toma de decisiones, liderazgo y salud mental. Para desarrollar la inteligencia emocional, se recomienda observar y nombrar nuestras emociones, aceptarlas sin juzgarlas, expresarlas de forma asertiva y respetuosa, buscar soluciones constructivas a los problemas, empatizar con los demás, escuchar activamente, dar y recibir feedback, y aprender de nuestros errores y éxitos.</p>
    </details>
    <details>
      <summary>¿Qué es el minfulness y cómo se puede practicar?</summary>
      <p>El mindfulness es una práctica que consiste en prestar atención al momento presente, con una actitud de apertura, curiosidad y aceptación. El mindfulness nos ayuda a reducir el estrés, la ansiedad, la depresión, el dolor y otros síntomas físicos y psicológicos. También nos ayuda a mejorar nuestra concentración, memoria, creatividad, autoconocimiento y bienestar. Para practicar el mindfulness, se recomienda hacer ejercicios de respiración, meditación, escaneo corporal, atención plena a las sensaciones, pensamientos y emociones, y aplicar el mindfulness a las actividades cotidianas, como comer, caminar, hablar, etc.</p>
    </details>
    <details>
      <summary>¿Qué es la resiliencia y comó se puede fortalecer?</summary>
      <p>La resiliencia es la capacidad de adaptarse y superar las adversidades, los traumas, las pérdidas y el estrés. La resiliencia nos ayuda a crecer, aprender y desarrollar nuestras fortalezas y recursos internos y externos. Para fortalecer la resiliencia, se recomienda tener una visión positiva de uno mismo y de la vida, aceptar la realidad y los cambios, buscar el sentido y el propósito de las experiencias difíciles, establecer y perseguir metas, cuidar la salud física y mental, cultivar el optimismo y la esperanza, y apoyarse en las redes sociales y familiares.</p>
    </details>
    <details>
      <summary>¿Qué es la asertividad y cómo se puede aprender?</summary>
      <p>La asertividad es la habilidad de expresar nuestros derechos, opiniones, necesidades y sentimientos de forma honesta, respetuosa y adecuada, sin agredir ni someterse a los demás. La asertividad nos ayuda a mejorar nuestra autoestima, comunicación, relaciones y satisfacción personal. Para aprender a ser asertivos, se recomienda identificar y modificar los pensamientos y creencias que nos limitan, practicar el lenguaje verbal y no verbal adecuado, usar técnicas como el disco rayado, el banco de niebla, el acuerdo asertivo, etc., y entrenarse en situaciones reales o simuladas.</p>
    </details>
    <details>
      <summary>¿Qué es la codependencia y cómo se puede superar?</summary>
      <p>La codependencia es una forma de relación disfuncional en la que una persona depende emocional, económica o psicológicamente de otra, y se sacrifica, se anula o se somete a ella, perdiendo su identidad, su autoestima y su autonomía. La codependencia nos impide tener relaciones sanas, equilibradas y satisfactorias. Para superar la codependencia, se recomienda reconocer y aceptar el problema, buscar ayuda profesional o de grupos de apoyo, establecer y respetar límites, recuperar el control de nuestra vida, desarrollar nuestra autoestima y autoconfianza, y aprender a amarnos y respetarnos a nosotros mismos.</p>
    </details>
    <details>
      <summary>¿Qué es el duelo y cómo se puede afrontar?</summary>
      <p>El duelo es el proceso de adaptación emocional que se produce tras la pérdida de un ser querido, una mascota, una relación, un trabajo, una salud, etc. El duelo implica experimentar una serie de emociones, como tristeza, rabia, culpa, miedo, negación, aceptación, etc., que pueden variar en intensidad, duración y orden. El duelo nos ayuda a elaborar la pérdida, a reorganizar nuestra vida y a seguir adelante. Para afrontar el duelo, se recomienda expresar y compartir nuestros sentimientos, buscar apoyo emocional, cuidar nuestra salud física y mental, realizar actividades que nos gusten y nos distraigan, honrar la memoria de lo perdido, y permitirnos sentir y sanar a nuestro ritmo.</p>
    </details>
    <details>
      <summary>¿Qué es la adicción y cómo se puede tratar?</summary>
      <p>La adicción es una enfermedad crónica que se caracteriza por la dependencia física, psicológica o social de una sustancia, una conducta o una persona, que genera una pérdida de control, una tolerancia, un síndrome de abstinencia y un deterioro de la salud y la calidad de vida. La adicción nos impide disfrutar de otras fuentes de placer, satisfacción y bienestar, y nos aísla de nuestro entorno. Para tratar la adicción, se recomienda reconocer y aceptar el problema, buscar ayuda profesional o de grupos de autoayuda, seguir un tratamiento farmacológico, psicológico o combinado, evitar los estímulos o situaciones de riesgo, desarrollar habilidades de afrontamiento, y contar con el apoyo de la familia y los amigos.</p>
    </details>
    <details>
      <summary>¿Qué es el trastorno obsesivo-compulsivo y cómo se puede controlar?</summary>
      <p>El trastorno obsesivo-compulsivo es un trastorno de ansiedad que se caracteriza por la presencia de pensamientos intrusivos, irracionales y recurrentes (obsesiones) que generan una gran angustia, y de comportamientos repetitivos, ritualizados y compulsivos (compulsiones) que se realizan para aliviar esa angustia. El trastorno obsesivo-compulsivo nos impide llevar una vida normal, ya que consume mucho tiempo, energía y recursos, y afecta a nuestra salud, trabajo, relaciones y ocio. Para controlar el trastorno obsesivo-compulsivo, se recomienda buscar ayuda profesional, seguir una terapia cognitivo-conductual o una terapia de exposición y prevención de respuesta, tomar medicación si es necesario, practicar técnicas de relajación, y educarse sobre el trastorno y sus consecuencias.</p>
    </details>
