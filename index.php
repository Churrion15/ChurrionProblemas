    <h1 id="causas">CAUSAS</h1>
    <h4 class="contenido_pagina">
      <p>
        Las causas de los problemas psicológicos son diversas y pueden implicar factores biológicos, psicológicos y sociales. Algunas de las principales causas son:
      </p>
      <img id="imgCausas" src="img/cerebro_y_corazon_son_la_misma_cosa_12.jpg" alt="" align="left" hspace="50" vspace="50">
      <ul>
        <li id="biologicos">
          Factores biológicos: Incluyen la genética, los desequilibrios químicos en el cerebro, las lesiones cerebrales, las infecciones o las enfermedades crónicas. Estos factores pueden predisponer a una persona a desarrollar un problema psicológico o desencadenarlo ante determinadas circunstancias.
        </li>
        
        <br />
        <li id="psicologicos">
          Factores psicológicos: Se refieren a la forma en que una persona piensa, siente y se comporta. Los traumas, el abuso, la negligencia, el estrés crónico, la baja autoestima o las dificultades para gestionar las emociones pueden contribuir a la aparición de problemas psicológicos.
        </li>
        <br />
        <li id="sociales">
          Factores sociales: Tienen que ver con el entorno y las relaciones de una persona. La pobreza, la violencia, la discriminación, el aislamiento social o la falta de apoyo pueden influir en la salud mental y aumentar el riesgo de desarrollar problemas psicológicos.
        </li>
      </ul>
      <hr>
    </h4>
    <h1 id="comoSeResuelve">CÓMO SE RESUELVE</h1>
    <h4 class="contenido_pagina">
      <p>
        Los problemas psicológicos se pueden tratar de diversas formas, dependiendo del tipo y la gravedad de cada caso. Algunos de los tratamientos más comunes son:
      </p>
      <img id="imgResolver" src="img/pinturas_de_locos_11.jpg.webp" alt="" align="right" hspace="50" vspace="50">
      <ul>
        <li id="Tfarmacologico">
          Tratamiento farmacológico: Consiste en el uso de medicamentos que ayudan a aliviar los síntomas de los problemas psicológicos, como los antidepresivos, los ansiolíticos, los antipsicóticos o los estabilizadores del estado de ánimo. Estos medicamentos deben ser recetados por un médico y supervisados regularmente.
        </li>
        <br />
        <li id="Psicoterapia">
          Psicoterapia: Es una forma de tratamiento que implica hablar con un profesional de la salud mental, como un psicólogo o un psiquiatra, para explorar y modificar los pensamientos, las emociones y los comportamientos que causan malestar. Hay diferentes tipos de psicoterapia, como la terapia cognitivo-conductual, la terapia de aceptación y compromiso, la terapia interpersonal o la terapia psicodinámica.
        </li>
        <br />
        <li id="Telectroconvulsiva">
          Terapia electroconvulsiva: Es un tratamiento que se utiliza en casos graves de depresión o trastornos psicóticos que no responden a otros tratamientos. Consiste en aplicar una corriente eléctrica al cerebro para provocar una convulsión controlada, que puede aliviar los síntomas.
        </li>
        <br />
        <li id="CRotros">
          Otros: Existen otras formas de tratamiento que pueden ser útiles para algunas personas, como la terapia ocupacional, la terapia de grupo, la terapia de familia, la terapia artística o la terapia asistida con animales. Además, adoptar hábitos de vida saludables, como hacer ejercicio, comer bien, dormir lo suficiente, evitar el alcohol y las drogas, y mantener una red de apoyo social, puede contribuir a mejorar la salud mental.
        </li>
      </ul>
