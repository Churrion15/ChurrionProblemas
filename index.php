    <h4 class="contenido_pagina">
      <p id="intro">
      Los problemas psicológicos son condiciones que afectan el funcionamiento normal de la mente y las emociones de una persona, causando malestar o dificultades en su vida cotidiana. Estos problemas pueden tener diversas causas, como biológicas, genéticas, ambientales o una combinación de estas. Los problemas psicológicos pueden manifestarse de diferentes formas, como ansiedad, depresión, trastornos del estado de ánimo, esquizofrenia y más.
      </p>
      <p>
      Los problemas psicológicos se pueden tratar con la ayuda de profesionales de la salud mental, que pueden ofrecer terapias, medicamentos o una combinación de ambos. La terapia consiste en enseñar habilidades y estrategias a las personas con problemas psicológicos, que les permitan crear nuevas reacciones emocionales y eliminar las anteriores, aquellas que ocasionaban el malestar.
      </p>
      <p>
      Los problemas psicológicos son afecciones reales y tratables, y no son una cuestión de voluntad o falta de fortaleza. Reconocer que los problemas psicológicos son condiciones legítimas es un paso fundamental para buscar ayuda y tratamiento adecuado. Si usted o alguien que conoce sufre de algún problema psicológico, no dude en consultar con un profesional calificado. La salud mental es tan importante como la salud física, y merece atención y cuidado.
      </p>
    </h4>
    <hr>
    <h1 id="tipos">TIPOS</h1>
    <h4 class="contenido_pagina">
      <p>
        Existen diversos problemas psicológicos, y cada uno tiene sus propias características, causas y tratamientos. A continuación, se dará una breve descripción de algunos de los más comunes:
      </p>
      <ul>
        <li id="trastornoAnsiedad">
          Trastornos de ansiedad: Son aquellos que se caracterizan por una preocupación excesiva, miedo o nerviosismo ante situaciones o estímulos que no representan un peligro real. Dentro de esta categoría hay varios subtipos, como el trastorno de pánico, el trastorno obsesivo-compulsivo, las fobias o la fobia social.
        </li>
        <br />
        <li id="trastornoAnimo">
          Trastornos del estado de ánimo: Son aquellos que afectan la forma en que una persona se siente, ya sea con tristeza, apatía, irritabilidad o euforia. Los más conocidos son la depresión y el trastorno bipolar.
        </li>

        <div class="imagenens">
          <img id="imgTipos" src="img/Autoretrato_16.jpg" alt="" align="left" hspace="50" vspace="50">
        </div>

        <br />
        <li id="trastornoAlimenticio">
          Trastornos de la alimentación: Son aquellos que implican una alteración en la forma de comer, ya sea por comer demasiado, muy poco o de forma selectiva. Los más frecuentes son la anorexia, la bulimia y el trastorno por atracón.
        </li>
        <br />
        <li id="trastornoPersonalidad">
          Trastornos de la personalidad: Son aquellos que se refieren a patrones de pensamiento, sentimiento y comportamiento que son rígidos, inflexibles y que dificultan la adaptación a las diferentes situaciones de la vida. Algunos ejemplos son el trastorno límite de la personalidad, el trastorno narcisista de la personalidad o el trastorno antisocial de la personalidad.
        </li>
        <br />
        <li id="trastornoEstresPostraumatico">
          Trastorno de estrés postraumático: Es un trastorno que se produce después de haber vivido o presenciado un evento traumático, como un accidente, una agresión, una catástrofe natural o una guerra. Las personas con este trastorno suelen tener recuerdos intrusivos, pesadillas, hipervigilancia o evitación de situaciones relacionadas con el trauma.
        </li>
        <br />
        <li id="trastornosPsicoticos">
          Trastornos psicóticos: Son aquellos que afectan la percepción de la realidad, causando alucinaciones, delirios o pensamientos desorganizados. El más conocido es la esquizofrenia.
        </li>
      </ul>
